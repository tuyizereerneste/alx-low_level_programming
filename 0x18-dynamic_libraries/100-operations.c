#include "main.h"
#include <stdio.h>

/**
 * add - Function that add two numbers
 * @a: first number
 * @b: second number
 * Return: always 0 when success
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function that substract two numbers
 * @a: first number
 * @b: second number
 * Return: always 0 when success
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * multiply - Function that multiplies two numbers
 *  @a: first number
 *  @b: second number
 *  Return: always 0 when success
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * divide - Function that divides two numbers
 * @a: first number
 * @b: second number
 * Return: always 0 when success
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * modulus - Function that find modulus two numbers
 * @a: first number
 * @b: second number
 * Return: always 0 when success
 */

int mod(int a, int b)
{
	return (a % b);
}
